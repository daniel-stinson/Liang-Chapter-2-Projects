package chapter2Examples;

public class Ch2_Checkpoint {

	public static void main(String[] args) {
		// Declare variables
		double miles = 100;
		final double KILOMETERS_PER_MILE = 1.609;
		double kilometers;
		
		
		// Compute
		kilometers = miles * KILOMETERS_PER_MILE;
		
		// Display
		System.out.println(kilometers);

	}

}
